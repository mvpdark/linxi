from __future__ import annotations

# ============================================================
# gpt-image-2 官方尺寸规格 (OpenAI 官方文档)
# ============================================================
# 约束规则:
#   1. 最大边长: 不超过 3840px
#   2. 边长粒度: 必须为 16px 的倍数
#   3. 比例限制: 长短边比例不超过 3:1
# ============================================================

SUPPORTED_SIZES = [
    # 1K (短边基准 1024)
    "1024x1024", "1024x1536", "1536x1024", "1024x1824", "1824x1024",
    "1024x1360", "1360x1024", "1024x2384", "2384x1024",
    # 2K (短边基准 2048)
    "2048x2048", "2048x3072", "3072x2048", "2048x3648", "3648x2048",
    "2048x2736", "2736x2048", "2048x3840", "3840x2048",
    # 4K (最大边 3840)
    "3072x3072", "2560x3840", "3840x2560", "2160x3840", "3840x2160",
    "2880x3840", "3840x2880", "1648x3840", "3840x1648",
    # legacy small
    "512x512", "512x768", "768x512",
]

# 分辨率档位
RESOLUTION_OPTIONS = ["1K", "2K", "4K"]

# 比例选项 - 涵盖所有常用比例 (长短边比均不超过3:1)
RATIO_OPTIONS = [
    "1:1",    # 方形
    "2:3",    # 竖版照片
    "3:2",    # 横版照片
    "9:16",   # 手机竖屏
    "16:9",   # 标准宽屏
    "3:4",    # 竖版
    "4:3",    # 横版
    "21:9",   # 超宽银幕 (2.33:1 < 3:1, 合法)
    "9:21",   # 超高竖版 (2.33:1 < 3:1, 合法)
]

# 分辨率 + 比例 → 实际尺寸映射
# 所有尺寸均为16的倍数，最大边不超过3840px，长短边比不超过3:1
SIZE_MAP = {
    # 1K (短边基准 1024)
    ("1K", "1:1"):  "1024x1024",
    ("1K", "2:3"):  "1024x1536",
    ("1K", "3:2"):  "1536x1024",
    ("1K", "9:16"): "1024x1824",
    ("1K", "16:9"): "1824x1024",
    ("1K", "3:4"):  "1024x1360",
    ("1K", "4:3"):  "1360x1024",
    ("1K", "21:9"): "1024x2384",    # 1024 * 21/9 = 2389.3 → 2384 (16的倍数)
    ("1K", "9:21"): "2384x1024",

    # 2K (短边基准 2048)
    ("2K", "1:1"):  "2048x2048",
    ("2K", "2:3"):  "2048x3072",
    ("2K", "3:2"):  "3072x2048",
    ("2K", "9:16"): "2048x3648",
    ("2K", "16:9"): "3648x2048",
    ("2K", "3:4"):  "2048x2736",
    ("2K", "4:3"):  "2736x2048",
    ("2K", "21:9"): "2048x3840",    # 2048 * 21/9 = 4778 → 截断到3840 (最大边限制)
    ("2K", "9:21"): "3840x2048",

    # 4K (最大边 3840)
    ("4K", "1:1"):  "3072x3072",    # 受最大边3840限制, 4K方形用3072
    ("4K", "2:3"):  "2560x3840",
    ("4K", "3:2"):  "3840x2560",
    ("4K", "9:16"): "2160x3840",
    ("4K", "16:9"): "3840x2160",
    ("4K", "3:4"):  "2880x3840",
    ("4K", "4:3"):  "3840x2880",
    ("4K", "21:9"): "1648x3840",    # 3840 * 9/21 = 1645.7 → 1648 (16的倍数)
    ("4K", "9:21"): "3840x1648",
}


def get_size(resolution: str, ratio: str) -> str:
    """根据分辨率和比例获取实际API尺寸字符串。

    Args:
        resolution: 分辨率档位 ("1K", "2K", "4K")
        ratio: 比例 ("1:1", "2:3", "3:2", "9:16", "16:9", "3:4", "4:3", "21:9", "9:21")

    Returns:
        尺寸字符串，如 "1024x1024"
    """
    return SIZE_MAP.get((resolution, ratio), "1024x1024")
